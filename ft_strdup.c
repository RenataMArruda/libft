#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*ptr;
	int		len;
	int		i;

	len = ft_const_strlen(str);
	ptr = malloc((len + 1) * sizeof(char));
	if (ptr == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		ptr[i] = str[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
